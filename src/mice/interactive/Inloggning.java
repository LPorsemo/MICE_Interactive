    /* Matcha inskrivet lösenord mot databasens för att logga in */
    private boolean kollaAnvandare(String aid, String losenord){
        
            boolean matchar = false;
            String query = "SELECT LOSENORD FROM ADMINISTRATOR where AID = '" + aid + "';";
         try {   
            String rattlosenord = databasen.fetchSingle(query);
            
            if (losenord.equals(rattlosenord))
            {
                matchar = true;
            }
            
        } catch (InfException ex) {
            System.out.println(ex.getMessage());
        }
        return matchar;
    }
    private String konverteraTillAid(String anvNamn)
    {
        
        String aid = "";
        
       
        String query = "SELECT AID FROM ANSTALLD WHERE ANVNAMN = '" + anvNamn + "'";
        try{
            aid = databasen.fetchSingle(query);
        }
        catch(InfException ex)
        {
            System.out.println("BLÄV FÄL!");
        }
        
        
        return aid;
    }
     
   
    
    
